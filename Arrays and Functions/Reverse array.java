// Reverse an array


public class MyClass {
    
       
      
       public static void main(String args[]) {
           int arr[] = {21,12,45,34};
           
           for(int i = arr.length - 1; i >= 0; i--)
           {
               System.out.println(arr[i]);
           
           }
        }
           
       
       
     
}
    
